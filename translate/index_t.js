var language=[];
language["serbian"]=serbian;
language["english"]=english;



$(document).ready(function(){
    let lng=localStorage.getItem("language");
    if(lng==null){
        lng="serbian";
        localStorage.setItem("language",JSON.stringify(lng));
    }
    else lng=JSON.parse(lng);
    lng=language[lng];
    for(let key in lng){
        if(key=="placeholders"){
            for(let k in lng[key]){
               // alert(lng[key][k]);
                $("#"+k).attr("placeholder",lng[key][k]);
            }
        }
        else if(key=="classes"){
            for(let k in lng[key]){
                // alert(lng[key][k]);
                 $("."+k).html(lng[key][k]);
             }
        }
        else if (key=="optGroup"){
            for(let k in lng[key]){
                // alert(lng[key][k]);
                 $("#"+k).attr("label",lng[key][k]);
             }
        }
        else $("#"+key).html(lng[key]);
    }
     //about
    if(localStorage.getItem("current")==""){
        $("#btnabout").attr("href","index.html").text(lng.btnabout2);
    }
    else{
        $("#btnabout").attr("href","addRecipe.html").text(lng.btnabout);
    }
    //about end
   
    $("#srpski").click(function(){
        localStorage.setItem("language",JSON.stringify("serbian")); 
        window.location.reload();
    });
    $("#engleski").click(function(){
        localStorage.setItem("language",JSON.stringify("english")); 
        window.location.reload();
    });
    let hist=localStorage.getItem("history");
    if(hist==null)return;
    $("#detailsRec").text(lng.detailsRec[parseInt(hist)]);
    

    
    
});